package com.github.yumelira.yumebox.service

import java.net.URLDecoder
import java.nio.charset.StandardCharsets
import java.util.Locale

internal object ProfileNameUtils {

    private val AUTO_NAME_SET = setOf(
        "newprofile",
        "remotefile",
        "新配置",
        "新配置文件",
    )

    fun isAutoGeneratedProfileName(name: String): Boolean {
        val normalized = name.trim()
        if (normalized.isBlank()) return true
        if (normalized.startsWith("temp_", ignoreCase = true)) return true

        val lowered = normalized.lowercase(Locale.getDefault())
            .replace(" ", "")
            .replace("_", "")
            .replace("-", "")

        return lowered in AUTO_NAME_SET
    }

    fun extractSourceBaseName(source: String): String? {
        return runCatching {
            source
                .substringBefore('?')
                .substringBefore('#')
                .substringAfterLast('/')
                .substringBeforeLast('.')
                .let { URLDecoder.decode(it, StandardCharsets.UTF_8.name()) }
                .trim()
                .takeIf { it.isNotBlank() }
        }.getOrNull()
    }

    fun resolveDisplayName(name: String, source: String): String {
        if (!isAutoGeneratedProfileName(name)) return name
        return extractSourceBaseName(source) ?: name
    }
}
